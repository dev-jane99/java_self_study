package interfaceTest3;

public class Movie {
	private String name;
	private char theater;
	private int hour;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public char getTheater() {
		return theater;
	}
	public void setTheater(char theater) {
		this.theater = theater;
	}
	public int getHour() {
		return hour;
	}
	public void setHour(int hour) {
		this.hour = hour;
	} 
	
	}

